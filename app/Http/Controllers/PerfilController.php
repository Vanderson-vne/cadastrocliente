    public function senha(Request $request){
        $this->validate($request,[
            'password' => 'required|string|min:8|confirmed',
        ]);

        $id = Auth::id();

        User::where('id',$id)->update([
            'password' => bcrypt($request->password)
        ]);

        return back()->with(
            'success','Senha atualizada com Sucesso'
